                <div class="date">8.06.2017 </div>        <h2 class="news_title">Как выбрать ноутбук?</h2>
                <div class="page_text">
                    <img class="main-image" src="images/news/20170816184257126.jpg" alt="Как выбрать ноутбук?" />
                    <p>При выборе ноутбука большинство людей, как правило, обращают внимание на один или несколько факторов исходя их тех задач, которые перед ними стоят. Это не всегда верно. Учитывать характеристики необходимо комплексно, так как многие из них взаимосвязаны. Перед выбором ноутбука стоит задать себе несколько вопросов, которые помогут вам понять, на что обратить внимание при покупке.</p>
                    <p><img src="/upload/image/%D0%9A%D0%B0%D0%BA%20%D0%B2%D1%8B%D0%B1%D1%80%D0%B0%D1%82%D1%8C%20%D0%BD%D0%BE%D1%83%D1%82%D0%B1%D1%83%D0%BA.png" alt="Как выбрать ноутбук " width="600" height="456" /></p>
                    <h4><strong> Как я буду использовать свое устройство?</strong></h4>
                    <p>Пожалуй, самый важный вопрос. Не редко при покупке ноутбука люди твердо уверены, что никогда не будут использовать его для чего-либо, кроме сёрфинга в интернете и просмотра видео, к тому же цена может быть относительно доступной. Такой вариант возможен, однако жизнь непредсказуема. Через какое-то время возможно, что вам захочется обработать фото или видео для семейного архива, установить игру или вашим новым хобби станет веб разработка. Малопроизводительное устройство с такими задачами не справится, а потому вам либо придётся от них отказаться, либо купить другое устройство соответствующее вашим требованиям.</p>
                    <h4><strong>Где я буду использовать свой ноутбук? </strong></h4>
                    <p>Если вы собираетесь часто брать ноутбук с собой, вам так же стоит учесть несколько факторов. Корпус и размеры. Ультратонкие и легкие ноутбуки и устройства 2 в 1 конечно имеют свои преимущества, однако их общий недостаток - это непрочный корпус и не редко плохое качество сборки. Весят они в 2 раза меньше чем стандартный ноутбук, однако при частой транспортировке подвержены большему риску повреждений. Для сравнения, ноутбуки бизнес класса имеют корпус из металла или усиленного пластика с ребрами жесткости. Для таких ноутбуков падение с высоты одного метра или удары не критичны. Ноутбуки защищены от воздействия пыли и влаги, а потому идеально подходят для частых транспортировок.</p>
                    <h4><strong> Выбор процессора </strong></h4>
                    <p>Главное на что стоит обратить внимание при выборе ноутбука. Процессор отвечает за многозадачность и производительность. В наши дни компания Intel безусловный лидер в производстве процессоров, а потому рассмотрим именно их.</p>
                    <p><br />Intel atom: максимально бюджетный процессор для населения развивающихся стран, его используют в максимально удешевленных ноутбуках, он плохо справляется с любыми даже самыми слабыми задачами.</p>
                    <p><br />Intel Core 2 Duo: больше не выпускается, однако довольно распространённая линейка. В зависимости от конфигурации устройства подойдет для несложных задач, офиса или дома. Отлично подойдет для торговли, авто диагностики и полиграфии, так как в этих сферах даже в наши дни производительные устройства не требуются.</p>
                    <p>Intel Celeron: урезанный процессор, немного мощнее чем Intel atom едва справляется с самыми простыми задачами, но намного медленнее чем другие процессоры, часто заставляет пользователя ожидать выполнения задач.</p>
                    <p><br />Intel Pentium: процессор среднего уровня, подойдет для не требовательных пользователей и не сложных задач. Для решения задач сложнее офисных не подойдет. Рекомендуется как бюджетная альтернатива более топовых процессоров для не требовательных пользователей.</p>
                    <p><br />Intel I Core. Существует 3 линейки: Core i3, Core i5, Core i7. Так что же выбрать?</p>
                    <p><br />Core i3. Процессоры для многозадачности. Предназначены для работы, однако это не означает, что с играми они не справятся. Если вы хотите комфортно работать с несколькими программами одновременно или для работы вам необходимо большое количество активных вкладок в браузере, этот процессор то что вам нужно.</p>
                    <p><br />Core i5. Многозадачность + игры. Большинство современных игр в разделе рекомендуемые параметры указывают именно этот процессор. При этом комфортная работа, как в интернете, так и с требовательными приложениями вам обеспечена.</p>
                    <p><br />Core i7. Самая мощная линейка процессоров. Современные игры, требовательное программное обеспечение, 3D моделирование и многое другое. Эта линейка откроет перед вами максимум возможностей. Конечно при выборе также стоит учесть поколение линейки I Core. Принцип простой: чем старше поколение, тем лучше. Однако и цена за процессоры последних поколений довольно высокая. В реальной жизни оптимальным решением для офиса и дома станет первое-третье поколение, а для любителей игр и тех, кто работает с требовательным софтом, третье-пятое. К тому же существуют специальные программы для оценки процессоров, SysMark и 3DMark. Такие программы помогут вам выбрать устройство, если перед вами стоят узкие и определенные задачи.<br /> Оперативная память или ОЗУ отвечает за быстродействие приложений. Оптимальный вариант 4 Gb. Если в конфигурации вашего ноутбука больше это безусловно плюс, а вот если меньше, то стоит определиться хватит ли вам такого объёма.</p>
                    <h4><strong>Жесткий диск </strong></h4>
                    <p>Самые распространённые на сегодня жесткие диски Sata 2.5. Они до сих пор активно используются и производятся составляя конкуренцию более новым SSD. В чем же разница? Sata существенно дешевле и это главное преимущество. Выбор как новых, так и БУ жестких дисков Sata практически любого объёма огромен. Из минусов подверженность физическим повреждениям. Небольшое падение или удар как жесткого диска, так и ноутбука с ним внутри может привести жесткий диск в негодность. SSD падений и ударов боятся не так сильно. Скорость обмена данными у них значительно больше, однако это не означает что скорость работы приложений так же увеличится. Фактически, приложения будут быстрее загружаться при этом работать будут так же. Объём жесткого диска стоит выбирать такой что бы было достаточно для программного обеспечения и файлов которые вам часто необходимы. Для хранения фото, видео и прочих файлов которые не нужны вам ежедневно, существуют внешние жесткие диски или облачные хранилища. Это удобно и предоставит вам доступ к файлам с разных устройств. Поэтому покупка жесткого диска с огромным объёмом памяти не всегда является лучшим решением.</p>
                    <h4><strong>Видеокарта</strong></h4>
                    <p>90% процентов пользователей уверены, что им необходимо устройство исключительно с дискретной/внешней видеокартой. Конечно дискретная видеокарта имеет свои преимущества. Современные игры, фото, видео-монтаж и прочие подобные программы работают значительно быстрее, однако есть и минусы. Главный из них цена. Ноутбук с дискретной видеокартой будет стоить на порядок дороже. Второй автономность. Дискретная видеокарта значительно уменьшает время работы устройства от батареи. К тому же многие интегрированные видеокарты прекрасно справляются с популярными играми, например World of Tanks. Так же они позволят вам работать в графических и видео редакторах, в том случае если объем обрабатываемого материала не слишком большой.</p>
